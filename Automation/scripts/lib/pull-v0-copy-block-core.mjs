/**
 * Shared: build **v0 Copy Block** from v0 API + optional Airtable PATCH.
 * Used by ledger-pull-v0-copy-block.mjs and ledger-to-v0.mjs (LEDGER_PULL_COPY_BLOCK).
 */
const AIRTABLE_API = 'https://api.airtable.com/v0'
export const DEFAULT_COPY_BLOCK_MAX_CHARS = 95_000

/**
 * Path segment after /chat/ — often the raw API id (e.g. jxZVMz9crqY).
 * v0 may also use slug-prefixed URLs: relay-visual-system-2-iBAVip45yiX → API id iBAVip45yiX.
 */
export function chatIdFromWebUrl(url) {
  const s = String(url || '').trim()
  const m = s.match(/v0\.app\/chat\/([^/?#]+)/i)
  if (!m) return ''
  const raw = m[1].trim()
  const parts = raw.split('-')
  if (parts.length >= 2) {
    const last = parts[parts.length - 1]
    if (/^[A-Za-z0-9]{10,14}$/.test(last)) return last
  }
  return raw
}

export function copyBlockMaxCharsFromEnv() {
  const raw = process.env.COPY_BLOCK_MAX_CHARS?.trim()
  if (!raw) return DEFAULT_COPY_BLOCK_MAX_CHARS
  const n = Number(raw)
  return Number.isFinite(n) && n > 1000 ? n : DEFAULT_COPY_BLOCK_MAX_CHARS
}

export function copyBlockPollMsFromEnv() {
  const raw = process.env.LEDGER_COPY_BLOCK_POLL_MS?.trim()
  const n = Number(raw)
  return Number.isFinite(n) && n >= 500 ? n : 3000
}

/** Max time to poll for version files after async create. 0 = single fetch only. */
export function copyBlockPollMaxMsFromEnv() {
  const raw = process.env.LEDGER_COPY_BLOCK_POLL_MAX_MS?.trim()
  if (raw === '0') return 0
  const n = Number(raw)
  if (Number.isFinite(n) && n >= 0) return n
  return 90_000
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms))
}

function requireEnv(name) {
  const v = process.env[name]
  if (!v?.trim()) throw new Error(`Missing required env: ${name}`)
  return v.trim()
}

/**
 * @param {string} [previewUrl] — from v0 `latestVersion.demoUrl`; omitted or empty = do not clear existing Airtable value
 */
export async function airtablePatchCopyBlock(baseId, tableId, recordId, copyBlock, previewUrl) {
  const token = requireEnv('AIRTABLE_API_KEY')
  const url = `${AIRTABLE_API}/${baseId}/${tableId}`
  /** @type {Record<string, string>} */
  const fields = { 'v0 Copy Block': copyBlock }
  const p = typeof previewUrl === 'string' ? previewUrl.trim() : ''
  if (p) fields['v0 Preview URL'] = p
  const res = await fetch(url, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${token}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ records: [{ id: recordId, fields }] }),
  })
  if (!res.ok) {
    const body = await res.text()
    throw new Error(`Airtable patch (v0 Copy Block) failed: ${res.status} ${body}`)
  }
  return res.json()
}

function guessFence(path) {
  const p = path.toLowerCase()
  if (p.endsWith('.tsx')) return 'tsx'
  if (p.endsWith('.ts')) return 'ts'
  if (p.endsWith('.jsx')) return 'jsx'
  if (p.endsWith('.js')) return 'js'
  if (p.endsWith('.css')) return 'css'
  if (p.endsWith('.json')) return 'json'
  if (p.endsWith('.md')) return 'md'
  return 'text'
}

function formatFilesCopyBlock(chatId, versionId, versionStatus, files, generatorLabel) {
  const header = [
    `<!-- Auto-generated by ${generatorLabel} — re-run ledger-pull-v0-copy-block or a new ledger create with LEDGER_PULL_COPY_BLOCK to refresh. -->`,
    '',
    `# v0 file export`,
    '',
    `- **chatId:** ${chatId}`,
    `- **versionId:** ${versionId}`,
    `- **status:** ${versionStatus}`,
    `- **files:** ${files.length}`,
    '',
    '---',
    '',
  ].join('\n')

  let body = ''
  for (const f of files) {
    const name = f.name || '(unnamed)'
    const content = typeof f.content === 'string' ? f.content : ''
    body += `## ${name}\n\n\`\`\`${guessFence(name)}\n${content}\n\`\`\`\n\n`
  }
  return header + body
}

function formatMessagesFallback(chatId, messages) {
  const assistant = (messages || []).filter((m) => m.role === 'assistant' && (m.content || '').trim())
  const header = [
    '<!-- Fallback: no version files in API response; concatenating assistant messages. Re-run ledger-pull-v0-copy-block after generation completes. -->',
    '',
    `# v0 message export (fallback)`,
    '',
    `- **chatId:** ${chatId}`,
    '',
    '---',
    '',
  ].join('\n')
  let body = ''
  for (let i = 0; i < assistant.length; i++) {
    body += `## Assistant message ${i + 1}\n\n${assistant[i].content.trim()}\n\n---\n\n`
  }
  return header + body
}

export function truncateCopyBlock(s, max) {
  if (s.length <= max) return s
  return `${s.slice(0, max)}\n\n<!-- TRUNCATED: increase COPY_BLOCK_MAX_CHARS or split integration by file manually -->`
}

export async function collectVersionFiles(v0, chatId) {
  const chat = await v0.chats.getById({ chatId })
  const lv = chat.latestVersion

  if (lv?.id && lv.status === 'completed' && lv.files?.length) {
    return { versionId: lv.id, status: lv.status, files: lv.files, source: 'getById.latestVersion' }
  }

  if (lv?.id) {
    const ver = await v0.chats.getVersion({
      chatId,
      versionId: lv.id,
      includeDefaultFiles: true,
    })
    if (ver.files?.length) {
      return { versionId: ver.id, status: ver.status, files: ver.files, source: 'getVersion(latest)' }
    }
  }

  let cursor
  let all = []
  for (;;) {
    const list = await v0.chats.findVersions({ chatId, limit: 50, cursor })
    const batch = list.data ?? []
    all = all.concat(batch)
    if (!list.pagination?.hasMore || !list.pagination?.nextCursor) break
    cursor = list.pagination.nextCursor
  }

  const completed = all.filter((v) => v.status === 'completed').sort((a, b) => a.createdAt.localeCompare(b.createdAt))
  const pick = completed.length ? completed[completed.length - 1] : all.length ? all[all.length - 1] : null

  if (pick?.id) {
    const ver = await v0.chats.getVersion({
      chatId,
      versionId: pick.id,
      includeDefaultFiles: true,
    })
    if (ver.files?.length) {
      return { versionId: ver.id, status: ver.status, files: ver.files, source: 'getVersion(scanned)' }
    }
  }

  if (chat.messages?.length) {
    return {
      versionId: lv?.id || 'n/a',
      status: lv?.status || 'messages-only',
      files: [],
      messages: chat.messages,
      source: 'messages-fallback',
    }
  }

  return { versionId: lv?.id || 'n/a', status: lv?.status || 'unknown', files: [], source: 'empty' }
}

/**
 * Poll until version files exist, time out, or messages fallback appears.
 */
export async function collectVersionFilesWithPoll(v0, chatId, pollMs, maxWaitMs) {
  let attempt = await collectVersionFiles(v0, chatId)
  if (attempt.files?.length) return attempt
  if (maxWaitMs <= 0) return attempt

  const deadline = Date.now() + maxWaitMs
  while (Date.now() < deadline) {
    await sleep(pollMs)
    attempt = await collectVersionFiles(v0, chatId)
    if (attempt.files?.length) return attempt
  }
  return attempt
}

export function shouldPullCopyBlockAfterLedger() {
  const raw = process.env.LEDGER_PULL_COPY_BLOCK?.trim().toLowerCase()
  return raw === '1' || raw === 'true' || raw === 'yes' || raw === 'on'
}

/**
 * @param {object} opts
 * @param opts.v0
 * @param {string} opts.chatId
 * @param {number} [opts.maxChars]
 * @param {number} [opts.pollMs] - 0 skips polling after first try
 * @param {number} [opts.pollMaxMs]
 * @param {string} [opts.generatorLabel]
 */
export function composeCopyBlockText(opts) {
  const { chatId, collected, maxChars, generatorLabel = 'ledger-pull-v0-copy-block' } = opts
  let text
  if (collected.files?.length) {
    text = formatFilesCopyBlock(
      chatId,
      collected.versionId,
      collected.status,
      collected.files,
      generatorLabel,
    )
  } else if (collected.messages?.length) {
    text = formatMessagesFallback(chatId, collected.messages)
  } else {
    const err = new Error(
      'No files or messages returned from v0. Wait for generation to complete, or run ledger-pull-v0-copy-block later.',
    )
    err.code = 'COPY_BLOCK_EMPTY'
    throw err
  }
  return truncateCopyBlock(text, maxChars ?? DEFAULT_COPY_BLOCK_MAX_CHARS)
}

/**
 * Full sync: collect (with optional poll) → compose → PATCH Airtable (unless dryRun).
 * @returns {Promise<{ source: string, fileCount: number, charCount: number, written: boolean }>}
 */
export async function syncCopyBlockToAirtable({
  v0,
  baseId,
  tableId,
  recordId,
  chatId,
  maxChars,
  dryRun = false,
  pollMs = 0,
  pollMaxMs = 0,
  generatorLabel = 'ledger-pull-v0-copy-block',
}) {
  let collected
  try {
    collected = await collectVersionFilesWithPoll(v0, chatId, pollMs, pollMaxMs)
  } catch (e) {
    const msg = e instanceof Error ? e.message : String(e)
    if (msg.includes('not_found') || msg.includes('404')) {
      throw new Error(
        `v0 chat not found (${chatId}). Update **v0 Chat URL** or V0_API_KEY (npm run v0-verify-key).`,
      )
    }
    if (msg.includes('403') || msg.includes('forbidden') || msg.includes('chat_forbidden')) {
      throw new Error(
        `v0 chat not accessible (${chatId}): private or wrong workspace for this API key. In v0: set privacy to **unlisted** (or share with the same account as V0_API_KEY), or switch Vercel/v0 team to match the chat. Verify: npm run v0-verify-key -- ${chatId}`,
      )
    }
    throw e
  }

  const fileCount = collected.files?.length ?? 0
  let copyBlock
  try {
    copyBlock = composeCopyBlockText({
      chatId,
      collected,
      maxChars: maxChars ?? copyBlockMaxCharsFromEnv(),
      generatorLabel,
    })
  } catch (e) {
    if (e && typeof e === 'object' && 'code' in e && e.code === 'COPY_BLOCK_EMPTY') {
      const err = new Error(
        `${e.message} (source=${collected.source}, files=${fileCount})`,
      )
      err.code = 'COPY_BLOCK_EMPTY'
      throw err
    }
    throw e
  }

  const out = {
    source: collected.source,
    fileCount,
    charCount: copyBlock.length,
    written: false,
    /** @type {string | undefined} */
    preview: undefined,
    /** Set after Airtable write */
    previewUrlWritten: false,
    /** @type {string | undefined} */
    previewUrl: undefined,
  }

  if (dryRun) {
    out.preview = copyBlock.slice(0, 2500) + (copyBlock.length > 2500 ? '\n…' : '')
    return out
  }

  let demoUrl = ''
  try {
    const chat = await v0.chats.getById({ chatId })
    demoUrl = chat.latestVersion?.demoUrl?.trim() || ''
  } catch {
    /* copy block still useful without preview link */
  }

  await airtablePatchCopyBlock(baseId, tableId, recordId, copyBlock, demoUrl)
  out.written = true
  out.previewUrlWritten = Boolean(demoUrl)
  out.previewUrl = demoUrl || undefined
  return out
}
